typedef long long ll;
typedef long double ld;

const ll mod = 1e9+7;
const ll inf = 1LL<<60;
const ld ep = 0.0000001;
const ld pi = acos(-1.0);

ll convert(ll n){
    ll res = 0;
    while(n){
        res += n%10;
        n/= 10;
    }
    return res;
}

int main(){
    string num;
    cin >> num;
    if(num.size()==1){
        cout << 0 << endl;
        return 0;
    }
    vector<ll> cnt(10,0);
    for(auto c: num){
        cnt[int(c-'0')]++;
    }
    ll numb = 0;
    for(int i=0; i<10; i++){
        numb += i*cnt[i];
    }
    int res = 1;
    while(numb>=10){
        res++;
        numb = convert(numb);
    }
    cout << res << endl;
    return 0;
}
